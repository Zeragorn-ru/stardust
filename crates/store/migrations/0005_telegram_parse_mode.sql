-- Необязательный режим разметки для исходящих сообщений Telegram.
--
-- Большинство сообщений — простой текст. Но сообщения подтверждения входа
-- теперь содержат ник аккаунта и код, обёрнутый в <code>…</code> (моноширинный,
-- удобно копировать). Чтобы Telegram отрендерил такую разметку, при отправке
-- нужно передать parse_mode=HTML. Храним режим по конкретному сообщению, а не
-- глобально — иначе пришлось бы экранировать HTML во всех текстах.
--   NULL  — обычный текст (parse_mode не передаётся);
--   'HTML' — текст содержит HTML-разметку Telegram.
ALTER TABLE telegram_outbox
    ADD COLUMN IF NOT EXISTS parse_mode TEXT;
