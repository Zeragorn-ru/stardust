export const BADGE_ICONS: Record<string, string> = {
  "\u2B50": STAR,
  "\uD83D\uDD25": FIRE,
  "\uD83D\uDDE1": SWORD,
  "\uD83D\uDC8E": GEM,
  "\uD83E\uDDEA": POTION,
  "\uD83D\uDEE1": SHIELD,
  "\u26CF": PICKAXE,
  "\uD83C\uDFC6": TROPHY,
  "\uD83D\uDC51": CROWN,
  "\u2764": HEART,
  "\uD83D\uDC80": SKULL,
  "\uD83C\uDF1F": GLOWING_STAR,
};

export function getBadgeIcon(emoji: string): string | null {
  const stripped = stripVS16(emoji);
  return BADGE_ICONS[stripped] ?? null;
}
