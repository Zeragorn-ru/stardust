const VS16 = "\uFE0F";

function stripVS16(emoji: string): string {
  return emoji.replace(new RegExp(VS16, "g"), "");
}

function px(coords: string): string {
  return coords
    .split("\n")
    .map((l) => l.trim())
    .filter(Boolean)
    .map((l) => {
      const [x, y, w, h] = l.split(/[,\s]+/).map(Number);
      return `<rect x="${x}" y="${y}" width="${w}" height="${h}"/>`;
    })
    .join("\n");
}

function svg(inner: string): string {
  return `<svg viewBox="0 0 30 30" fill="currentColor" xmlns="http://www.w3.org/2000/svg">\n${inner}\n</svg>`;
}

